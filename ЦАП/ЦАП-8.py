def main(number):
    # Извлечение битовых полей из входного числа
    T6 = (number >> 28) & 0b11
    T5 = (number >> 25) & 0b11
    T4 = (number >> 24) & 0b11
    T2 = (number >> 15) & 0b11
    T1 = (number >> 8) & 0b11

    # Формирование выходного числа
    output = 0
    output |= (T4 & 0b11) << 35  # Поле T4 на позиции 35-36
    output |= (T6 & 0b11) << 28   # Поле T6 на позиции 28-29
    output |= (T1 & 0b11) << 18   # Поле T1 на позиции 18-19
    output |= (T2 & 0b11) << 9    # Поле T2 на позиции 9-10
    output |= (T5 & 0b11) << 2     # Поле T5 на позиции 2-3

    # Преобразование в шестнадцатеричную строку
    hex_str = "0x{:x}".format(output)
    return hex_str

print(main(82212717073))  # Ожидаемый результат: '0x990004609'
print(main(75108933842))  # Ожидаемый результат: '0x8b0034987'
print(main(35757636677))  # Ожидаемый результат: '0x14200114f4'