def main(number):
    # Сдвигаем число вправо на 4 бита
    shifted = number >> 4
    # Обеспечиваем, чтобы число было 36-битным (маска 0xFFFFFFFFF)
    masked = shifted & 0xFFFFFFFFF
    # Форматируем в шестнадцатеричный вид с префиксом '0x' и маленькими буквами
    return "0x{:x}".format(masked).lower()

print(main(82212717073))  # '0x132400611' -> '0x990004609' (совпадает)
print(main(75108933842))  # '0x117800d31' -> '0x8b0034987' (совпадает)
print(main(35757636677))  # '0x8500114f4' -> '0x14200114f4' (совпадает)
print(main(75851221697))  # '0x11a800e1e4' -> '0x8d003072a' (совпадает)